SELECT CONCAT(EMPLOYEE.FIRST_NAME,' ', EMPLOYEE.LAST_NAME) AS `Nome completo`,
J.JOB_TITLE AS Cargo,
HISTORY_JOB.START_DATE  AS `Data de in√≠cio do cargo`,
DEP.DEPARTMENT_NAME AS `Departamento`
FROM hr.employees AS EMPLOYEE
INNER JOIN hr.job_history AS HISTORY_JOB
ON EMPLOYEE.EMPLOYEE_ID = HISTORY_JOB.EMPLOYEE_ID
INNER JOIN hr.jobs AS J 
ON J.JOB_ID = HISTORY_JOB.JOB_ID 
INNER JOIN hr.departments AS DEP 
ON HISTORY_JOB.DEPARTMENT_ID = DEP.DEPARTMENT_ID
ORDER BY `Nome completo` DESC, Cargo ASC;
