SELECT CONCAT(FIRST_NAME, " ", LAST_NAME) AS "Nome completo",
JOBS.JOB_TITLE AS 'Cargo',
START_DATE AS 'Data de in√≠cio do cargo',
DEPART.DEPARTMENT_NAME AS 'Departamento'
FROM hr.job_history H
JOIN hr.jobs JOBS ON H.JOB_ID = JOBS.JOB_ID
JOIN hr.departments DEPART ON H.DEPARTMENT_ID = DEPART.DEPARTMENT_ID
JOIN hr.employees EMP ON H.EMPLOYEE_ID = EMP.EMPLOYEE_ID
ORDER BY CONCAT(EMP.FIRST_NAME,' ',EMP.LAST_NAME) DESC, JOBS.JOB_TITLE;
