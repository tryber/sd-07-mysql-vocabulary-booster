SELECT 
CONCAT(EMP.FIRST_NAME, ' ', EMP.LAST_NAME) "Nome Completo",
JOB.JOB_TITLE Cargo,
HIST.START_DATE "Data de in√≠cio do cargo",
DEP.DEPARTMENT_NAME Departamento
FROM hr.job_history HIST
INNER JOIN hr.employees EMP
ON HIST.EMPLOYEE_ID = EMP.EMPLOYEE_ID
INNER JOIN hr.departments DEP
ON HIST.DEPARTMENT_ID = DEP.DEPARTMENT_ID
INNER JOIN hr.jobs JOB
ON HIST.JOB_ID = JOB.JOB_ID
ORDER BY CONCAT(EMP.FIRST_NAME, ' ', EMP.LAST_NAME) DESC, Cargo;
