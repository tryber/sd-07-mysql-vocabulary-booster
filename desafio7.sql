SELECT UCASE(CONCAT(EMPLOYEE.FIRST_NAME,' ', EMPLOYEE.LAST_NAME)) AS `Nome completo`,
HISTORY_JOB.START_DATE  AS `Data de início`,
EMPLOYEE.SALARY AS `Salário`
FROM hr.employees AS EMPLOYEE
INNER JOIN hr.job_history AS HISTORY_JOB ON EMPLOYEE.EMPLOYEE_ID = HISTORY_JOB.EMPLOYEE_ID
WHERE MONTH(HISTORY_JOB.START_DATE) IN(1,2,3)
ORDER BY `Nome completo`, `Data de início`;
