SELECT
  UCASE(CONCAT(EMPLOYEES.FIRST_NAME, ' ', EMPLOYEES.LAST_NAME)) AS 'Nome completo',
  JOBHISTORY.START_DATE AS 'Data de início',
  EMPLOYEES.SALARY AS 'Salário'
FROM hr.employees AS EMPLOYEES
INNER JOIN hr.job_history AS JOBHISTORY
ON JOBHISTORY.EMPLOYEE_ID = EMPLOYEES.EMPLOYEE_ID
WHERE MONTH(JOBHISTORY.START_DATE) BETWEEN 1 AND 3
ORDER BY 1, 2;
