SELECT 
UCASE(CONCAT(EMP.FIRST_NAME, ' ', EMP.LAST_NAME)) "Nome completo",
HIST.START_DATE "Data de início",
EMP.SALARY Salário
FROM hr.job_history HIST
INNER JOIN hr.employees EMP
ON HIST.EMPLOYEE_ID = EMP.EMPLOYEE_ID
WHERE MONTH(HIST.START_DATE) BETWEEN 1 AND 3
ORDER BY UCASE(CONCAT(EMP.FIRST_NAME, ' ', EMP.LAST_NAME)), HIST.START_DATE;
