SELECT
  UPPER (CONCAT(FIRST_NAME, ' ', LAST_NAME )) AS 'Nome completo',
  START_DATE AS 'Data de início',
  SALARY AS 'Salário'
FROM
  hr.employees AS EMP
INNER JOIN
  hr.job_history AS JB_H
ON
  EMP.EMPLOYEE_ID = JB_H.EMPLOYEE_ID
ORDER BY 
  UPPER (CONCAT(FIRST_NAME, ' ', LAST_NAME )) ASC, START_DATE ASC;
