SELECT UCASE(CONCAT(EMPLOYEES.FIRST_NAME, ' ', EMPLOYEES.LAST_NAME)) AS 'Nome completo',
JOB_HIST.START_DATE AS 'Data de início do cargo',
EMPLOYEES.SALARY AS 'Salário'
FROM hr.job_history AS JOB_HIST
INNER JOIN hr.employees AS EMPLOYEES
ON JOB_HIST.EMPLOYEE_ID = EMPLOYEES.EMPLOYEE_ID
WHERE MONTH(JOB_HIST.START_DATE) IN (01, 02, 03)
ORDER BY `Nome Completo`, `Data de início do cargo`;
