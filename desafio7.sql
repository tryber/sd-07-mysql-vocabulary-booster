SELECT
	UCASE(CONCAT(EMPLOYEES.FIRST_NAME, ' ', EMPLOYEES.LAST_NAME)) AS 'Nome completo',
    JOB_HIST.START_DATE AS 'Data de início',
    EMPLOYEES.SALARY AS 'Salário'
FROM
	hr.employees AS EMPLOYEES
		INNER JOIN
	hr.job_history AS JOB_HIST ON EMPLOYEES.EMPLOYEE_ID = JOB_HIST.EMPLOYEE_ID
WHERE MONTH(JOB_HIST.START_DATE) BETWEEN 1 AND 3
ORDER BY `Nome completo` ASC, `Data de início` ASC;
