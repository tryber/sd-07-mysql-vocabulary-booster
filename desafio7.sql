USE hr;
SELECT 
	UCASE(CONCAT(EMPLOYEES.FIRST_NAME, ' ', EMPLOYEES.LAST_NAME)) AS 'Nome completo',
    HIS_JOB.START_DATE AS 'Data de início',
    EMPLOYEES.SALARY AS 'Salário'
FROM employees AS EMPLOYEES
INNER JOIN job_history AS HIS_JOB ON HIS_JOB.EMPLOYEE_ID = EMPLOYEES.EMPLOYEE_ID
WHERE MONTH(HIS_JOB.START_DATE) IN (01, 02, 03)
ORDER BY `Nome completo`, `Data de início`;
