SELECT ucase(CONCAT(E.FIRST_NAME, ' ', E.LAST_NAME)) AS 'Nome completo',
I.START_DATE AS 'Data de início',
E.SALARY AS 'Salário'
FROM hr.employees AS E
INNER JOIN hr.job_history AS I
ON E.EMPLOYEE_ID = I.EMPLOYEE_ID
WHERE MONTH(I.START_DATE) BETWEEN 1 AND 3
ORDER BY ucase(CONCAT(E.FIRST_NAME, ' ', E.LAST_NAME)), I.START_DATE;
