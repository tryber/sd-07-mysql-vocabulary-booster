SELECT J.JOB_TITLE, ROUND(AVG(SALARY),2) AS `Média salarial`,
CASE 
WHEN ROUND(AVG(SALARY),2) <= 5800.00 THEN 'Junior'
WHEN ROUND(AVG(SALARY),2) <= 7500.00  THEN 'Senior'
WHEN ROUND(AVG(SALARY),2) <= 10500.00 THEN 'Pleno'
ELSE 'CEO' END AS 'Senioridade'
FROM hr.employees AS E
INNER JOIN hr.jobs AS J 
ON E.JOB_ID = J.JOB_ID
GROUP BY J.JOB_TITLE
ORDER BY `Média salarial`, J.JOB_TITLE;
