SELECT J.JOB_TITLE AS Cargo, cast(AVG(E.SALARY) AS DECIMAL(10,2)) AS "Média salarial",
CASE
  WHEN AVG(E.SALARY) > 10500 THEN "CEO"
  WHEN AVG(E.SALARY) > 7500 THEN "Sênior"
  WHEN AVG(E.SALARY) > 5800 THEN "Pleno"
  WHEN AVG(E.SALARY) > 1999 THEN "Júnior"
END AS "Senioridade"
FROM jobs AS J
INNER JOIN employees AS E
ON J.JOB_ID = E.JOB_ID
GROUP BY J.JOB_ID
ORDER BY AVG(E.SALARY) ASC, J.JOB_TITLE;
