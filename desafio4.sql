SELECT B.JOB_TITLE AS 'Cargo', ROUND(B.MIN_SALARY + B.MAX_SALARY/2, 2) AS 'Média salarial',
CASE
WHEN ROUND(B.MIN_SALARY + B.MAX_SALARY/2, 2) BETWEEN 2000 AND 5800 THEN 'Júnior'
WHEN ROUND(B.MIN_SALARY + B.MAX_SALARY/2, 2) BETWEEN 5801 AND 7500 THEN 'Pleno'
WHEN ROUND(B.MIN_SALARY + B.MAX_SALARY/2, 2) BETWEEN 7501 AND 10500 THEN 'Sênior'
ELSE 'CEO'
END AS 'Senioridade'
FROM hr.employees AS A
INNER JOIN hr.jobs AS B
ORDER BY ROUND(B.MIN_SALARY + B.MAX_SALARY/2, 2), B.JOB_TITLE;
