USE hr;
DELIMITER $$
CREATE PROCEDURE buscar_media_por_cargo(IN jobName VARCHAR(100))
BEGIN
SELECT ROUND(AVG(EMPLOYEES.SALARY), 2) AS 'MÃ©dia salarial'
FROM hr.employees AS EMPLOYEES
INNER JOIN hr.jobs AS JOBS ON EMPLOYEES.JOB_ID = JOBS.JOB_ID
WHERE jobName = JOBS.JOB_TITLE
GROUP BY EMPLOYEES.JOB_ID;
END $$
DELIMITER ;
